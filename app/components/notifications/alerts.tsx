import { Bounce, toast } from 'react-toastify';

export const Error = (typeError:string) => toast.error(typeError, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
    transition: Bounce,
});

